# int data type any integer number
print("\n")
print("int data type")
age = 21
print(age)
age = age + 1
print(age)
age += 3
print(age)
print("you age is ",age)
print("are you "+str(age)+" years old")
print(type(age))


# float data type a decimal number like "97.5"
print("\n")
print("float data type")
height = 250.5
print(height)
print("Your height is "+str(height)+"cm")
